    public void criarBotoes(){
        
        try {
            ControleMesa controleMesa = new ControleMesa();
            Icon iconeMesa = new ImageIcon(getClass().getResource("/org/rangolibre/img/botoes/24x24/mesa.png"));  
            ArrayList<Mesa> listaMesa = new ArrayList<Mesa>();
            listaMesa = controleMesa.obterMesas();
            //Crio o objeto BotaoMesa com o tamanho necess√°rio
            BotaoMesa[] listaBotaoMesa = new BotaoMesa[listaMesa.size()];
            
            int linhas = (listaMesa.size()/8)+1;
            painelMesas.setLayout(new GridLayout(linhas,8));
            
            
            for (int i = 0; i < listaMesa.size(); i++) {
                listaBotaoMesa[i] = new BotaoMesa( new JButton(listaMesa.get(i).getId()),listaMesa.get(i) ); 
                listaBotaoMesa[i].bt.setIcon(iconeMesa);
                listaBotaoMesa[i].bt.setVerticalTextPosition(SwingConstants.BOTTOM);
                listaBotaoMesa[i].bt.setHorizontalTextPosition(SwingConstants.CENTER);
                painelMesas.add(listaBotaoMesa[i].bt);
                
                if (listaMesa.get(i).getStatus().equals("L")) {
                     listaBotaoMesa[i].bt.setBackground(Color.GREEN);
                } else {
                     listaBotaoMesa[i].bt.setBackground(Color.RED);
                }
                
                
                listaBotaoMesa[i].bt.addActionListener(new java.awt.event.ActionListener() {
                    public void actionPerformed(java.awt.event.ActionEvent evt) {
                        try {
                            ActionPerformed(evt);
                        } catch (PropertyVetoException ex) {
                            Logger.getLogger(FramePedidoBuscaMesa.class.getName()).log(Level.SEVERE, null, ex);
                        }
            }
        });
   
            }

        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());    
        }    
    
    }
    
    private void ActionPerformed(java.awt.event.ActionEvent evt) throws PropertyVetoException {
        fm.carregarCampoMesa(evt.getActionCommand().trim());
        this.setClosed(true);
    }
    
    
    

